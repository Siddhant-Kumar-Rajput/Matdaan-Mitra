function t(key, replacements = {}) {
  const lang = window.LANG || 'en';
  let str = I18N[lang]?.[key] || I18N['en']?.[key] || key;
  Object.entries(replacements).forEach(([k, v]) => {
    str = str.replace(`{${k}}`, v);
  });
  return str;
}

async function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const replacements = el.dataset.i18nVars ? JSON.parse(el.dataset.i18nVars) : {};
    el.textContent = t(key, replacements);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    el.setAttribute('aria-label', t(el.dataset.i18nAria));
  });

  // Fallback: Use Google Translate for everything else if translate.js is loaded
  if (typeof autoTranslatePage === 'function') {
    await autoTranslatePage();
  }
}

document.addEventListener('DOMContentLoaded', applyTranslations);
