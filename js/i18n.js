/**
 * Gets a UI string in the current session language.
 * Falls back to English if key not found in chosen language.
 *
 * @param {string} key - The i18n key
 * @returns {string} Translated string
 */
function t(key) {
  const lang = getSession().language || 'en';
  return I18N[lang]?.[key] || I18N['en']?.[key] || key;
}

/**
 * Updates all elements with data-i18n attribute with translated text.
 */
function updateUIStrings() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (key) {
      el.textContent = t(key);
    }
  });
  document.documentElement.lang = getSession().language || 'en';
}

document.addEventListener('DOMContentLoaded', updateUIStrings);
